print("\nIn Reverse Order:", places_to_visit)

places_to_visit.reverse()
print("\nIn Original Order:", places_to_visit)

places_to_visit.sort()
print("\nUsing sort:", places_to_visit)

places_to_visit.sort(reverse=True)
